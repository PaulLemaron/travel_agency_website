<h2>Babu Safaris Says</h2>
<p>
	
Kenya is known as the Jewel of Africa; it is the vast home of a wide variety of predators and herbivores, migrating through the Mara Plains every year.<br>
<br>
It is rich in tribal culture and practice, where the brightly dresses Maasai people live by the earth, following the rains and avidly conserving Kenya's wildlife.<br>
<br>
In addition to game-rich national parks and reserves, which are protected and valued as the wealth of the country, Kenya's Mombasa coast offers some of the most beautiful beach holiday spots on the Indian Ocean shoreline.<br>
<br>
The Kenyan Rift Valley is the vast location of a number of salt lakes, where enormous flocks of flamingos paint the waters pink, and hot springs burst through the surface of the unexplored lakes creating a spectacle quite unrivalled.

 
