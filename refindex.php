   <h2>The Jewel of Africa.</h2>

<p>
	Kenya is East Africa's premier safari destination and fondly regarded as the 'Jewel of Africa'. We can't deny that Kenya has it all, including incredible wildlife, beautiful mountains, deserts, salt lakes, cities, traditional culture, and modern arts.<br>
<br>
With one-tenth of all land in Kenya designated as national parks and reserves, Kenya truly is a place for devoted safari guests. The Masai Mara, possibly Kenya's most well known, has some of the biggest predator populations in Africa, so if a Big Cat safari is what you are after, this is the place to be.<br>
<br>
Over 50 parks and reserves cover habitats from mountain forest to desert, and there are six marine parks in the Indian Ocean. Although the Masai Mara is Kenya's most famous attraction, Kenya boasts many other national parks, outstanding wildlife, villages, and beaches.
